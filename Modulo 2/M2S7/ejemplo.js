var numero = parseInt(prompt("Ingrese un número"));

switch (numero) { 
    case 1: 
        alert("la opción ingresada es 1");
        break; 
    case 2: 
        alert("la opción ingresada es dos");
        break; 
    case 3: 
        alert("la opción ingresada es tres");
        break; 
    default: 
        alert("La opción ingresada es distinto a 1, 2 o 3"); 
        break;
}